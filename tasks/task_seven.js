//using the switch statment
const dayOfWeek = "Monday";

switch (dayOfWeek) {
  case "Monday":
    console.log("Its Monday, Hit The ground Running");
    break;
  case "Tuesday":
    console.log("Its Tuesday, Continue Running");
    break;
  case "Wednesday":
    console.log("Do not stop Running");
    break;
  case "Thursday":
    console.log("Today is games You Know");
    break;
  case "Friday":
    console.log("Thank God its Friday");
    break;
  case "Saturday":
    console.log("Hit the Gym, Do something Fun");
    break;
  case "Sunday":
    console.log("Remeber to keep the sabbath day HOLY");
    break;
  default:
    console.log("Please enter a valid Day of the week");
}